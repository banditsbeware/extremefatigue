Hyakujo wished to send a monk to open a new monastery. He told his pupils that whoever answered a question most ably would be appointed.</br>
Placing a water vase on the ground, he asked: "Who can say what this is without calling its name?"</br>
The chief monk said: "No one can call it a wooden shoe."</br>
Isan, the cooking monk, tipped over the vase with his foot and went out.</br>
Hyakujo smiled and said: "The chief monk loses."</br>
And Isan became the master of the new monastery.
