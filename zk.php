The student Doko came to a Zen master, and said: "I am seeking the truth. In what state of mind should I train myself, so as to find it?"</br>
Said the master, "There is no mind, so you cannot put it in any state. There is no truth, so you cannot train yourself for it."</br>
"If there is no mind to train, and no truth to find, why do you have these monks gather before you every day to study Zen and train themselves for this study?"</br>
"But I haven't an inch of room here," said the master, "so how could the monks gather? I have no tongue, so how could I call them together or teach them?"</br>
"Oh, how can you lie like this?" asked Doko.</br>
"But if I have no tongue to talk to others, how can I lie to you?" asked the master.</br>
Then Doko said sadly, "I cannot follow you. I cannot understand you."</br>
"I cannot understand myself," said the master.
